# Data Types

# Built-in data types
integer = 42
float_num = 3.14
complex_num = 1 + 2j
string = "Python"
boolean = True
none_type = None

print(type(integer), type(float_num), type(complex_num))
print(type(string), type(boolean), type(none_type))

# Type conversion
print(int(3.14), float(5), str(100))