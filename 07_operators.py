# Operators

# Arithmetic
print(10 + 5, 10 - 5, 10 * 5, 10 / 5, 10 // 5, 10 % 5, 10 ** 2)

# Comparison
print(10 > 5, 10 == 5, 10 != 5)

# Logical
print(True and False, True or False, not True)

# Assignment
x = 10
x += 5
print(x)

# Membership and Identity
lst = [1,2,3]
print(2 in lst)
print(lst is lst)