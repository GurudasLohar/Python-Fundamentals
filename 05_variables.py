# Variables

# Variables are containers for storing data values
name = "Alice"
age = 25
height = 5.7
is_student = True

print(f"Name: {name}, Age: {age}, Height: {height}, Student: {is_student}")

# Dynamic typing - can change type
x = 10
x = "Hello"
print(x)

# Multiple assignment
a, b, c = 1, 2, 3
print(a, b, c)